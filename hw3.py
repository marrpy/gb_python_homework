# Для чисел в пределах от 20 до 240 найти числа, кратные 20 или 21. Необходимо решить задание в одну строку.
# Подсказка: использовать функцию range() и генератор.

data = [el for el in range(20, 240) if el % 20 == 0]
new_data = [el for el in range(20, 240) if el % 21 == 0]
all_data = [el for el in range(20, 240) if el % 21 and 20 == 0]
print(f'Список чисел, кратных 20: {data}')
print(f'Список чисел, кратных 21: {new_data}')
print(f'Список чисел, кратных 20 и 21: {all_data}')

